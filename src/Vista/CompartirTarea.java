    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!TfCompartir.getText().isEmpty()) {
            TareaUsuario TarUsu = new TareaUsuario();
            Ctrl_Compartir controlador = new Ctrl_Compartir();

            TarUsu.setUsuario(IdUsuarioActual);
            TarUsu.setTarea(IdTareaSeleccionada);
            TarUsu.setEstado(1);
            TarUsu.setUsuarioACompartir(TfCompartir.getText().trim());

            if (controlador.Guardar(TarUsu)) {
                JOptionPane.showMessageDialog(null, "Compartido");
            } else {
                JOptionPane.showMessageDialog(null, "Error al Compartir la tarea");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una Tarea");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


