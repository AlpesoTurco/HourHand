    public void Registro() {

        if (!TfNombreReg.getText().isEmpty() && !TfApellidoReg.getText().isEmpty() && !TfUsuarioReg.getText().isEmpty() && !TfContrasenaReg.getText().isEmpty() && !PsConfContrasenaReg.getText().isEmpty() && !TfNumTelReg.getText().isEmpty()) {
            //Llenar datos de personas

            if (PsConfContrasenaReg.getText().equals(TfContrasenaReg.getText())) {
                Ctrl_Usuario controlUsu = new Ctrl_Usuario();
                Usuario Usu = new Usuario();
                Ctrl_Persona controladorperson = new Ctrl_Persona();
                Persona person = new Persona();
                //Datos de persona
                person.setIdPersona(Perso);
                person.setNombre(TfNombreReg.getText().trim());
                person.setApellidos(TfApellidoReg.getText().trim());
                person.setNumero(TfNumTelReg.getText().trim());

                if (controladorperson.Guardar(person)) {
                    System.out.println("GUARDADO CORRECTAMENTE");
                } else {
                    System.out.println("Error al capturar datos");
                }

                if (!controlUsu.ExisteUsuario(IdUsu.trim())) {
                    Usu.setIdUsuario(IdUsu);
                    Usu.setTipoUsuario("Usuario");
                    Usu.setUsuario(TfUsuarioReg.getText().trim());
                    Usu.setPassword(TfContrasenaReg.getText().trim());
                    Usu.setPersona(Perso);
                    Usu.setEstado(1);
                    if (controlUsu.NuevoUsuario(Usu)) {
                        JOptionPane.showMessageDialog(null, "REGISTRO EXITOSO");
                        TfNombreReg.setText("");
                        TfApellidoReg.setText("");
                        TfUsuarioReg.setText("");
                        TfContrasenaReg.setText("");
                        PsConfContrasenaReg.setText("");
                        TfNumTelReg.setText("");
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al capturar datos");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El Usuario Existe");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Su contrase침a no coincide");
            }

        } else {
            JOptionPane.showMessageDialog(null, "COMPLETA LOS DATOS");
        }
    }
