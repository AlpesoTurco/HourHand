    private void CargarTablaMateria() {
        Connection cn = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "SELECT IdMaestro,Nombre,Descripcion,Estado FROM tb_maestros WHERE Usuario = '" + IdUsuarioActual + "';";
        Statement st;

        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            GestionarMaestros.TablaMaestros = new JTable(model);
            GestionarMaestros.jScrollPane1.setViewportView(GestionarMaestros.TablaMaestros);

            model.addColumn("IdMaestro");
            model.addColumn("Nombre");
            model.addColumn("Descripcion");
            model.addColumn("Estado");

            while (rs.next()) {
                Object fila[] = new Object[4];
                for (int i = 0; i < 4; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            cn.close();

        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla: " + e);
        }

        TablaMaestros.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                int fila_point = TablaMaestros.rowAtPoint(e.getPoint());
                int fila_columna = 0;

                if (fila_point > -1) {
                    IdMaestro = (int) model.getValueAt(fila_point, fila_columna);
                    EnviarDatosMateriaSeleccionada(IdMaestro);
                }
            }
        });

    }

    private void EnviarDatosMateriaSeleccionada(int IdMaestro) {
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pat = cn.prepareStatement(
                    "SELECT * FROM tb_maestros WHERE IdMaestro = '" + IdMaestro + "'");
            ResultSet rs = pat.executeQuery();

            if (rs.next()) {
                TfEditDesc.setText(rs.getString("Descripcion"));
            }
            cn.close();
        } catch (SQLException e) {
            System.out.println("Error cargar maestro: " + e);
        }
    }
