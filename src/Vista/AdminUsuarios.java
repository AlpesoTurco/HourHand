    private void BnGuadarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BnGuadarCambiosActionPerformed
        if (!LbNameUsuario.getText().isEmpty()) {
            Usuario Usu = new Usuario();
            Ctrl_AdministradorUsuarios controladmin = new Ctrl_AdministradorUsuarios();
            
            Usu.setTipoUsuario(CbxPrivilegios.getSelectedItem().toString());
            if (controladmin.PrivilegiosAdmin(Usu, controladmin.IdUsuario)) {
                JOptionPane.showMessageDialog(null, "Actualización Exitosa");
                controladmin.CargarTablaUsuaios();
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar datos");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un usuario");
        }
    }//GEN-LAST:event_BnGuadarCambiosActionPerformed


