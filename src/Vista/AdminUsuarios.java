    private void BnDarAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BnDarAltaActionPerformed
        if (!LbNameUsuario.getText().isEmpty()) {
            Usuario Usu = new Usuario();
            Ctrl_AdministradorUsuarios controladmin = new Ctrl_AdministradorUsuarios();

            if (controladmin.DarDeAlta(Usu, controladmin.IdUsuario)) {
                JOptionPane.showMessageDialog(null, "Usuario De Alta");
                controladmin.CargarTablaUsuaios();
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar el usuario");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un usuario");
        }
    }//GEN-LAST:event_BnDarAltaActionPerformed

