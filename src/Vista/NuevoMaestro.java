    private void BnGuardarMaestroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BnGuardarMaestroActionPerformed
        Maestro Profe = new Maestro();
        Ctrl_Maestro ControlProfes = new Ctrl_Maestro();
            
        if(TfNomMaestro.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "INGRESA DATOS COMPLETOS");
        }else{
            if(!ControlProfes.ExisteMaestro(TfNomMaestro.getText().trim())){
                Profe.setNombre(TfNomMaestro.getText().trim());
                Profe.setDescripcion(TaDescripcion.getText().trim());
                Profe.setUsuarioM(IdUsuarioActual);
                Profe.setEstado(1);
                if(ControlProfes.GuardarMaestro(Profe)){
                    JOptionPane.showMessageDialog(null, "DATOS GUARDADOS CORRECTAMENTE");
                    TfNomMaestro.setText("");
                    TaDescripcion.setText("");
                }else{
                    JOptionPane.showMessageDialog(null, "ERROR AL GUARDAR, VUELVA A INTENTARLO");
                }
            }else{
                JOptionPane.showMessageDialog(null, "LA MATERIA YA EXISTE!");
            }
        }
    }//GEN-LAST:event_BnGuardarMaestroActionPerformed


