    private void BnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BnActualizarActionPerformed
        if (!TfDescripcion.getText().isEmpty()) {
            Materia materia = new Materia();
            Ctrl_Materia controlmateria = new Ctrl_Materia();

            materia.setNombre(TfDescripcion.getText().trim());

            if (controlmateria.Actualizar(materia, controlmateria.IdMateria)) {
                JOptionPane.showMessageDialog(null, "Materia Actualizada");
                TfDescripcion.setText("");
                controlmateria.CargarTablaMateria();
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar la Materia");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una Materia");
        }
    }//GEN-LAST:event_BnActualizarActionPerformed

    private void BnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BnEliminarActionPerformed
        if (!TfDescripcion.getText().isEmpty()) {
            Materia materia = new Materia();
            Ctrl_Materia controlmateria = new Ctrl_Materia();

            materia.setNombre(TfDescripcion.getText().trim());

            if (!controlmateria.Eliminar(controlmateria.IdMateria)) {
                JOptionPane.showMessageDialog(null, "Materia Eliminada");
                TfDescripcion.setText("");
                controlmateria.CargarTablaMateria();
            } else {
                JOptionPane.showMessageDialog(null, "Error al eliminar la Materia");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una Materia");
        }
    }//GEN-LAST:event_BnEliminarActionPerformed


